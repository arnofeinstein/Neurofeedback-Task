function test_ml_log_trial_result()
%TEST_ML_LOG_TRIAL_RESULT Unit test for MonkeyLogic result normalization.

    params = default_params();
    trials = build_trial_table(params);
    ml_trial = ml_prepare_trial(trials(1), params);

    correct_result = ml_log_trial_result(ml_trial, struct( ...
        'response_angle', ml_trial.correct_target_angle, ...
        'reaction_time_ms', 321, ...
        'n_states_visited', 10, ...
        'last_state_name', 'ITI'));

    assert(strcmp(correct_result.final_outcome, 'correct'));
    assert(strcmp(correct_result.terminal_state, 'REWARD'));
    assert(correct_result.completed == true);
    assert(correct_result.response_correct == true);
    assert(correct_result.angular_error_deg == 0);
    assert(correct_result.trialerror_code == 0);
    assert(iscell(correct_result.bhv_variables));

    wrong_angles = params.sample_dirs(params.sample_dirs ~= ...
        mod(ml_trial.correct_target_angle, 360));
    wrong_angle = wrong_angles(1);

    wrong_result = ml_log_trial_result(ml_trial, struct( ...
        'final_outcome', 'wrong_target', ...
        'terminal_state', 'ERROR', ...
        'completed', false, ...
        'response_angle', wrong_angle, ...
        'reaction_time_ms', 280, ...
        'n_states_visited', 7, ...
        'last_state_name', 'WAIT_FOR_SACCADE'));

    assert(strcmp(wrong_result.final_outcome, 'wrong_target'));
    assert(strcmp(wrong_result.terminal_state, 'ERROR'));
    assert(wrong_result.completed == false);
    assert(wrong_result.response_correct == false);
    assert(wrong_result.trialerror_code == 6);

    no_response_result = ml_log_trial_result(ml_trial, struct( ...
        'final_outcome', 'no_response', ...
        'terminal_state', 'ERROR', ...
        'completed', false, ...
        'n_states_visited', 7, ...
        'last_state_name', 'WAIT_FOR_SACCADE'));

    assert(strcmp(no_response_result.final_outcome, 'no_response'));
    assert(isnan(no_response_result.response_angle));
    assert(no_response_result.response_correct == false);
    assert(no_response_result.trialerror_code == 1);

    disp('test_ml_log_trial_result passed');
end
